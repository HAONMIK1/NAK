package market.produser;

public class ProdUserBean {
	private int tnum;
	private String timg;
	private String tname;
	private String tcate;
	private String tlocation;
	private int tprice;
	private String tinfor;
	public int getTnum() {
		return tnum;
	}
	public void setTnum(int tnum) {
		this.tnum = tnum;
	}
	public String getTimg() {
		return timg;
	}
	public void setTimg(String timg) {
		this.timg = timg;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getTcate() {
		return tcate;
	}
	public void setTcate(String tcate) {
		this.tcate = tcate;
	}
	public String getTlocation() {
		return tlocation;
	}
	public void setTlocation(String tlocation) {
		this.tlocation = tlocation;
	}
	public int getTprice() {
		return tprice;
	}
	public void setTprice(int tprice) {
		this.tprice = tprice;
	}
	public String getTinfor() {
		return tinfor;
	}
	public void setTinfor(String tinfor) {
		this.tinfor = tinfor;
	}
}
